                <br><br><br><br><br>
                <h3>Qu'est-ce que la Journée R&Day ?</h3>
                <p class="text">La Journée Recherche et Développement (R&D ou encore R&Day) a pour objectif de créer un environnement propice à l'apprentissage, à la discussion et à l'interaction entre les étudiants, les enseignants et des professionnels de l'industrie.</p>
                <br>
                <h3>Qui sommes nous ?</h3>
                <p class="text">Nous sommes des étudiants en 2ème année de Licence d'informatique à l'Université de Franche-Comté de Besançon avec option Cursus Master en Ingénierie. Nous suivons un parcours universitaire scientifique de cinq ans (licence puis master) auquel sont ajoutés des cours de communication, connaissances du monde en entreprise, des projets individuels ou collectifs et stages, permettant au final d'obtenir, en plus du master classique, un diplome universitaire (DU) CMI.
                C'est dans cette optique que nous organisons cette journée R&Day.
                </p>
                <br>
                <h3>Quel est le sujet de la journée ?</h3>
                <p class="text">Cette journée de Recherche et Développement tournera autour du thème de la physique quantique et l'informatique, avec comme sujet : «Physique quantique et Informatique, le duo qui redéfinit l'avenir». Nous avons choisi d’explorer ce domaine en raison de son potentiel révolutionnaire. L’informatique quantique et la physique quantique ouvrent de nouvelles perspectives dans des domaines variés, allant de la cryptographie à l’intelligence artificielle. Leur développement soulève des défis scientifiques et technologiques majeurs tout en offrant des opportunités inédites pour façonner l’avenir de l’informatique et des sciences.</p>
                <br>
                <h3>Venez nombreux !</h3>
                <p class="text">
                Participer à une journée R&D sur la physique quantique et l’informatique peut être bénéfique tant sur le plan professionnel que personnel. Cet événement offre des opportunités d’apprentissage, et d’inspiration, tout en permettant de mieux comprendre les enjeux et les avancées du domaine quantique.</p>
                <p class="text"><strong>Nous vous attendons nombreux !</strong></p>
